# OPERADORES BIT A BIT
# --------------------

a = 0x09
b = 0x01

# AND
print(f'a & b = {a & b}')

# OR
print(f'a | b = {a | b}')

# XOR
print(f'a ^ b = {a ^ b}')

# NOT
print(f'~a = {~a}')

# desplazamiento a la derecha
print(f'b>>1 = {b>>1}')

# desplazamiento a la izquierda
print(f'b<<1 = {b<<1}')
