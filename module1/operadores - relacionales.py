# OPERADORES RELACIONALES
# -----------------------

a = 6
b = 3

# > Mayor que
print(f'a > b = {a > b}')

# < Menor que
print(f'a < b = {a < b}')

# == Igualdad
print(f'a == b = {a == b}')

# >= Mayor o igual
print(f'a >= b = {a >= b}')

# <= Menor o igual
print(f'a <= b = {a <= b}')

# != Diferente
print(f'a != b = {a != b}')