# OPERADORES LOGICOS
# ------------------

a = True
b = True
c = False

# AND, todos deben ser True
print('a and b and c =', a and b and c)
print('a & b & c =', a & b & c)

# OR, al menos uno debe ser True
print('a or b or c =', a or b or c)
print('a | b | c =', a | b | c)

# NOT
print('not a =', not a)
