# OPERADORES ARITMETICOS
# ----------------------
a = 6
b = 3

# Suma
print(f'a + b = {a+b}')

# Resta
print(f'a - b = {a-b}')

# Multiplicacion
print(f'a * b = {a*b}')

# Division
print(f'a / b = {a/b}')

# Division entera
print(f'a // b = {a//b}')

# Modulo
print(f'a % b = {a%b}')

# Exponenciacion
print(f'a ** b = {a**b}')