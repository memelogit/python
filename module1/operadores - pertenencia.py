# OPERADORES DE PERTENENCIA
# -------------------------

# Pertenencia en str
cadena = 'Anita lava la tina'
print(f"'Anita' in cadena? {'Anita' in cadena}")

# Pertenencia en listas
lista = [1, 3, 5, 7]
print(f'4 not in lista? {4 not in lista}')

# Pertenencia en tuplas
tupla = (2, 4, 6, 8)
print(f'4 in tupla? {4 in tupla}')

# Pertenencia en diccionarios
diccionario = {'num': 3, 'color': 'rojo'}
print(f'num in dicccionario? {"num" in diccionario}')